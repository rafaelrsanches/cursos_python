# Faça um algoritmo que solicite ao usuário informar 3 números. Em seguida, some-os e envie para a saída padrão a
# seguinte frase: "A soma total é: "

num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))
num3 = float(input("Digite o terceiro número: "))

sum = num1 + num2 + num3

print("A soma total é:", sum)
